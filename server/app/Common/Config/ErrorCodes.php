<?php

namespace App\Common\Config;

/**
 * 错误码配置
 * 定义系统中使用的错误码及其对应的错误消息
 */
class ErrorCodes
{
    /**
     * 获取错误码对应的错误消息
     *
     * @param int $code 错误码
     * @return string 错误消息，如果未找到则返回默认消息
     */
    public static function getMessage(int $code): string
    {
        $messages = self::getMessages();
        return $messages[$code] ?? "错误码: {$code}";
    }

    /**
     * 获取所有错误码消息映射
     *
     * @return array<int, string>
     */
    public static function getMessages(): array
    {
        return [
            // 通用错误 (10000-10099)
            10001 => '操作失败',
            10002 => '参数错误',
            10003 => '数据不存在',
            10004 => '数据已存在',
            10005 => '操作超时',
            10010 => '访问密码不正确',

            // 认证相关错误 (10100-10199)
            10101 => '操作失败',
            10102 => 'Token 无效',
            10103 => '需要管理员权限',
            10104 => '需要登录',
            10105 => '密码错误',
            10106 => '用户不存在',
            10107 => '用户已被禁用',

            // 验证码相关错误 (10200-10299)
            10206 => '验证码不正确',

            // 权限相关错误 (10300-10399)
            10301 => '没有权限',
            10302 => '没有访问权限',
            10303 => '您没有编辑权限',
            10304 => '您没有管理权限',
            10305 => '您没有删除权限',

            // 项目相关错误 (10400-10499)
            10401 => '项目不存在',
            10402 => '项目已被删除',
            10403 => '项目访问受限',

            // 页面相关错误 (10500-10599)
            10501 => '页面不存在',
            10502 => '页面已被删除',

            // 文件相关错误 (10600-10699)
            10601 => '文件上传失败',
            10602 => '文件不存在',
            10603 => '文件大小超限',
            10604 => '文件类型不支持',
        ];
    }
}

